# There is a difference in function but there is a difference in functions and methods

# append() method
nums = [1, 2, 3, 4]
nums.append(5)
print(nums)


# extend () method :
num1 = [1, 2, 3]
num1.extend([4, 5, 6])
print(num1)

# insert() method
nums = [1, 2, 3, 4, 5]
nums.insert(2, 3)
print(nums)


